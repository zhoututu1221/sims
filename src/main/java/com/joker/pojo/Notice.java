package com.joker.pojo;

public class Notice {
	
	Integer notice_id;
	String notice_user_id;
	String notice_content;
	String notice_time;
	String notice_isRead;
	public Integer getNotice_id() {
		return notice_id;
	}
	public void setNotice_id(Integer notice_id) {
		this.notice_id = notice_id;
	}
	public String getNotice_user_id() {
		return notice_user_id;
	}
	public void setNotice_user_id(String notice_user_id) {
		this.notice_user_id = notice_user_id;
	}
	public String getNotice_content() {
		return notice_content;
	}
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	public String getNotice_time() {
		return notice_time;
	}
	public void setNotice_time(String notice_time) {
		this.notice_time = notice_time;
	}
	public String getNotice_isRead() {
		return notice_isRead;
	}
	public void setNotice_isRead(String notice_isRead) {
		this.notice_isRead = notice_isRead;
	}
	
	

}
