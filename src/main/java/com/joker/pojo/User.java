package com.joker.pojo;

public class User {
	
	Integer user_id;
	String user_password;
	String user_picture;
	String user_name;
	String user_sector;
	String user_named;
	String user_time;
	String user_phone;
	String user_sex;
	
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer user_id) {
		this.user_id = user_id;
	}
	public String getUser_password() {
		return user_password;
	}
	public void setUser_password(String user_password) {
		this.user_password = user_password;
	}
	public String getUser_picture() {
		return user_picture;
	}
	public void setUser_picture(String user_picture) {
		this.user_picture = user_picture;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_sector() {
		return user_sector;
	}
	public void setUser_sector(String user_sector) {
		this.user_sector = user_sector;
	}
	public String getUser_named() {
		return user_named;
	}
	public void setUser_named(String user_named) {
		this.user_named = user_named;
	}
	public String getUser_time() {
		return user_time;
	}
	public void setUser_time(String user_time) {
		this.user_time = user_time;
	}
	public String getUser_phone() {
		return user_phone;
	}
	public void setUser_phone(String user_phone) {
		this.user_phone = user_phone;
	}
	public String getUser_sex() {
		return user_sex;
	}
	public void setUser_sex(String user_sex) {
		this.user_sex = user_sex;
	}

}
