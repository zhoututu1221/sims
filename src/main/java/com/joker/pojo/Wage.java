package com.joker.pojo;

public class Wage {
	
	Integer wage_id;
	String wage_userId;
	String wage_time;
	String wage_basic;
	String wage_bonus;
	
	public Integer getWage_id() {
		return wage_id;
	}
	public void setWage_id(Integer wage_id) {
		this.wage_id = wage_id;
	}
	public String getWage_userId() {
		return wage_userId;
	}
	public void setWage_userId(String wage_userId) {
		this.wage_userId = wage_userId;
	}
	public String getWage_time() {
		return wage_time;
	}
	public void setWage_time(String wage_time) {
		this.wage_time = wage_time;
	}
	public String getWage_basic() {
		return wage_basic;
	}
	public void setWage_basic(String wage_basic) {
		this.wage_basic = wage_basic;
	}
	public String getWage_bonus() {
		return wage_bonus;
	}
	public void setWage_bonus(String wage_bonus) {
		this.wage_bonus = wage_bonus;
	}
	
	

}
